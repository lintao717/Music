void oled_show_char(uint8_t x, uint8_t page, char ch)
{
    uint8_t i;
    uint8_t idx;
    uint16_t buf_top, buf_bot;

    if (ch < ' ' || ch > '~') ch = ' ';
    idx = (uint8_t)(ch - ' ');

    if (x >= OLED_WIDTH || page >= (OLED_HEIGHT / 8)) return;

    buf_top = (uint16_t)(page * OLED_WIDTH + x);
    buf_bot = (uint16_t)((page + 1) * OLED_WIDTH + x);

    for (i = 0; i < 8; i++)
    {
        if ((x + i) < OLED_WIDTH)
        {
            oled_buffer[buf_top + i] = OLED_FONT_8X16[idx][i];
            if ((page + 1) < (OLED_HEIGHT / 8))
                oled_buffer[buf_bot + i] = OLED_FONT_8X16[idx][8 + i];
        }
    }
}

void oled_show_string(uint8_t x, uint8_t page, const char *str)
{
    while (*str != '\0')
    {
        if (x > (OLED_WIDTH - 8))
        {
            x = 0;
            page = (uint8_t)(page + 2);
        }

        if (page > 6)
        {
            break;
        }

        oled_show_char(x, page, *str);
        x = (uint8_t)(x + 8);
        str++;
    }
}

